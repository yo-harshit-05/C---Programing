// Program 10 : To check a number is odd or even
#include<stdio.h>
void main(){
    int num;
    printf("Enter any number\n");
    scanf("%d",&num);
    if (num%2==0)
    {
        printf("The given number is even number\n");
    }
    else
    {
        printf("The given number is odd\n");
    }
}
